// lib/i18n.ts
export type Locale = "vi" | "en" | "ko" | "ja";
export const LOCALE_KEY = "hts-locale";

export const getLocale = (): Locale => {
  if (typeof window === "undefined") return "vi";
  const v = localStorage.getItem(LOCALE_KEY) as Locale | null;
  return v === "vi" || v === "en" || v === "ko" || v === "ja" ? v : "vi";
};

export const setLocaleStorage = (lc: Locale) => {
  localStorage.setItem(LOCALE_KEY, lc);
};

